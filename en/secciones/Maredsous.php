		<br /><br />
		This 19th-century beer is brewed in Maredsous Abbey (a Monastery located amidst the high mountains of Namur, Belgium) by Benedictine monks. In 1963 Moortgat Group started to support the monks from Maredsous Abbey so that their product portfolio was extended and added this line of abbey beers. 
		<br /><br />
		However, Maredsous Abbey beers are still brewed based on the original recipe that comes from the 7th-century Benedictine monks and the production is closely supervised by the Abbey community so that intact quality is kept without losing its origins.  The monksâ€™ best preserved secret is in the Abbey library where it is possible to find the recipes and secrets that make this beer so special.
		<br /><br />
		A large proportion of the profits made from the beer sales is given to charitable causes.
		<br /><br />
		<b>Maredsous Blonde:</b> was the first beer from the Abbey. At present monks still drink it at lunch. Its balance, which is malted, with a fruity taste and bitterness provided by hops, gives this beer all the refreshing qualities that make it a genuine Belgian Blonde.
		<br /><br />
		<b>Maredsous Brune:</b> at the beginning "Brune" or dark beers were only brewed for Christmas times. However, over the centuries, they were more and more extended to finally stay. With a creamy head and a dark color, this beer is recognized for its bouquet of aromas, where caramel prevails and it is also present in its taste although with hints of fruits as well.
		<br /><br />
		<b>Maredsous Tripel:</b>  this golden beer from the Abbey is high in alcohol, same as with all tripel beers. However, its good balance hides that alcohol content making it almost imperceptible. Its body is creamy with a long, well-balanced finish.
