		<a class="titulos_productos">MAREDSOUS</a>
		<br /><br />
		Cerveza que data del Siglo XIX, y que es producida en la abadía en Maredsous (Monasterio ubicado en las altas montañas de Namur, Bélgica) por los monjes benedictinos. En el año 1963 el Grupo Moortgat comenzó a apoyar a los monjes de esta abadía con el fin de aumentar su portafolio de produtos e incorporando esta linea de cervezas de abadía.
		<br /><br />
		Sin embargo, las cervezas de la abadía de Maredsous todavía se elaboran basadas en la receta de los monjes benedictinos del siglo VI y la producción está bajo la estrecha supervisión de la comunidad de la abadía, para que la calidad se mantenga intacta sin perder sus origenes. El secreto mejor guardado de los monjes está en la biblioteca de la abadía donde se encuentran las recetas y secretos que hacen tan particular esta cerveza.
		<br /><br />
		Una parte importante de las ganacías generadas por las cervezas Maredsous se usan para causas caritativas.
		<br /><br />
		<b>Maredsous Blonde:</b> Fue la primer cerveza de la abadía, hoy en día los monjes la siguen tomando durante el almuerzo. Su balance entre maltosidad, fruta y amargor proporcionado por el lúpulo dan a esta cerveza todas las cualidades refrescantes que hacen a una genuina Belgian Blonde.
		<br /><br />
		<b>Maredsous Brune:</b> Las cervezas "Brune" u oscuras sólo eran hechas, en un inicio, para la navidad. Sin embargo, con el correr de los siglos ganaron terreno y se quedaron. Con espuma cremosa y un color oscuro, es reconocida por su bouquet de aromas, donde destaca el caramlo. Su sabor, también dominado por el caramelo, se completa con toques de fruta.
		<br /><br />
		<b>Maredsous Tripel:</b> La tripel de la marca de Abadía es una cerveza de cuerpo dorado que, como toda tripel, tiene un alto contenido alcohólico. Sin embargo, su buen balance esconde este alcohol haciéndolo casi imperceptible. De cuerpo cremoso, tiene final en boca largo y muy balanceado.
